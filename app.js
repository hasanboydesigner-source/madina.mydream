function setSlide(index, event) {
    if (event) {
        event.preventDefault();
    }

    if (index === currentSlide) return;

    currentSlide = index;
    updateUI();
    resetProgressBar();
}

function nextSlide() {
    currentSlide = (currentSlide + 1) % travelData.length;
    updateUI();
    resetProgressBar();
}

function updateUI() {
    // 1. Update background slide active state
    bgImages.forEach((img, i) => {
        if (i === currentSlide) {
            img.classList.add("active");
        } else {
            img.classList.remove("active");
        }
    });

    // 2. Update vertical numbers active state
    numItems.forEach((num, i) => {
        if (i === currentSlide) {
            num.classList.add("active");
        } else {
            num.classList.remove("active");
        }
    });

    // 3. Update recommendation columns active state
    destColumns.forEach((col, i) => {
        if (i === currentSlide) {
            col.classList.add("active");
        } else {
            col.classList.remove("active");
        }
    });

    // 4. Update top navigation link active state
    navItems.forEach((nav, i) => {
        if (i === currentSlide) {
            nav.classList.add("active");
        } else {
            nav.classList.remove("active");
        }
    });

    // 5. Build dynamic content matching translation and slide
    updateDynamicContent();
}

