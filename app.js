function changeLanguage(lang) {
    currentLang = lang;
    localStorage.setItem("selectedLang", lang);

    // 1. Update language switcher buttons
    document.querySelectorAll(".lang-btn").forEach(btn => {
        if (btn.textContent.toLowerCase() === lang) {
            btn.classList.add("active");
        } else {
            btn.classList.remove("active");
        }
    });

    // 2. Translate all static HTML data-i18n elements
    document.querySelectorAll("[data-i18n]").forEach(element => {
        const key = element.getAttribute("data-i18n");
        if (translations[key] && translations[key][lang]) {
            // Handle audio play state texts specifically
            if (key === "music-on") {
                if (bgMusic && !bgMusic.paused) {
                    element.textContent = translations["music-off"][lang];
                } else {
                    element.textContent = translations["music-on"][lang];
                }
            } else {
                element.textContent = translations[key][lang];
            }
        }
    });

    // 3. Update dynamic elements
    updateDynamicContent();
}

// ==========================================================================
// THEME SWITCHER ENGINE
// ==========================================================================
function toggleTheme() {
    if (currentTheme === "dark") {
        currentTheme = "light";
    } else {
        currentTheme = "dark";
    }
    applyTheme();
}

function applyTheme() {
    localStorage.setItem("selectedTheme", currentTheme);
    
    if (currentTheme === "light") {
        document.body.classList.add("light-theme");
        sunIcon.style.display = "none";
        moonIcon.style.display = "block";
    } else {
        document.body.classList.remove("light-theme");
        sunIcon.style.display = "block";
        moonIcon.style.display = "none";
    }
}

