// ==========================================================================
// PROGRESS BAR & AUTO SLIDE LOGIC
// ==========================================================================
function startProgressBar() {
    let start = Date.now();
    
    if (progressInterval) {
        clearInterval(progressInterval);
    }

    progressInterval = setInterval(() => {
        let elapsed = Date.now() - start;
        let percentage = Math.min((elapsed / SLIDE_DURATION) * 100, 100);
        progressFill.style.width = `${percentage}%`;

        if (elapsed >= SLIDE_DURATION) {
            nextSlide();
        }
    }, 50);
}

function resetProgressBar() {
    progressFill.style.width = '0%';
    startProgressBar();
}

// ==========================================================================
// MUSIC PLAYER LOGIC
// ==========================================================================
function toggleMusic() {
    if (bgMusic.paused) {
        bgMusic.play()
            .then(() => {
                musicStatusText.textContent = translations["music-off"][currentLang];
                playIcon.textContent = "⏸";
            })
            .catch(error => {
                console.error("Audio playback failed:", error);
            });
    } else {
        bgMusic.pause();
        musicStatusText.textContent = translations["music-on"][currentLang];
        playIcon.textContent = "▶";
    }
}

